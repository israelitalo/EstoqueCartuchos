    private void jTableNotificacaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableNotificacaoMouseClicked
        
        if(evt.getClickCount() == 2){
            int linhaSelecionada = jTableNotificacao.getSelectedRow();
            
            String descricao = jTableNotificacao.getValueAt(linhaSelecionada, 3).toString();
            
            txtDescricao.setText(descricao);
            
        }
        
    }//GEN-LAST:event_jTableNotificacaoMouseClicked

    public void listarNotificacoes(){
        
        NotificacaoDao ntfd = new NotificacaoDao();
        
        List<Notificacao> lista = ntfd.listarNotificacoes();
        
        DefaultTableModel model = (DefaultTableModel) jTableNotificacao.getModel();
        
        model.setNumRows(0);
        
        for(Notificacao ntf: ntfd.listarNotificacoes()){
            model.addRow(new Object[]{
                ntf.getIdNotificacao(),
                ntf.getUsuario(),
                ntf.getTipoNotificacao(),
                ntf.getDescricao()
            });
        }
        
        
    }
