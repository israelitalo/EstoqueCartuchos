    public void listarImpressoras(){
        ImpressoraDao impDoa = new ImpressoraDao();
        
        List<Impressora> listaImpressoras = impDoa.selectImpressora();
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("CODIGO");
        modelo.addColumn("MODELO");
        modelo.addColumn("SERIE");
        modelo.addColumn("FABRICANTE");
        modelo.addColumn("SETOR");
        
        Object rowData[] = new Object[5];
        
        for(int i=0;i<listaImpressoras.size();i++){
            rowData[0] = listaImpressoras.get(i).getId_impressora();
            rowData[1] = listaImpressoras.get(i).getModelo();
            rowData[2] = listaImpressoras.get(i).getSerie();
            rowData[3] = listaImpressoras.get(i).getFabricante();
            rowData[4] = listaImpressoras.get(i).getSetor();
            
            modelo.addRow(rowData);
        }
        
        tabelaImpressora.setModel(modelo);
        
        tabelaImpressora.getColumnModel().getColumn(0).setPreferredWidth(0);
        tabelaImpressora.getColumnModel().getColumn(1).setPreferredWidth(150);
        tabelaImpressora.getColumnModel().getColumn(2).setPreferredWidth(50);
        tabelaImpressora.getColumnModel().getColumn(3).setPreferredWidth(0);
        tabelaImpressora.getColumnModel().getColumn(4).setPreferredWidth(60);
    }
    
