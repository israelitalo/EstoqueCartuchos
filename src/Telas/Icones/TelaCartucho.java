    private void comboBoxImpressoraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxImpressoraActionPerformed
        if(!comboBoxImpressora.getSelectedItem().equals("Impressora")){
            comboBoxSetor.removeAllItems();
            CartuchoDao cd = new CartuchoDao();
            String impressora = (String) comboBoxImpressora.getSelectedItem();
            int idImpressora = cd.getIdJcomboBoxImpressora(impressora);
            int idSetor = cd.getIdSetorImpressora(idImpressora);
            String setor = cd.getSetorCartucho(idSetor);
            comboBoxSetor.addItem(setor);
            comboBoxSetor.setSelectedItem(setor);
        }else{
            comboBoxSetor.removeAllItems();
            comboBoxSetor.addItem("Setor");
            comboBoxSetor.setSelectedItem("Setor");
        }

    }//GEN-LAST:event_comboBoxImpressoraActionPerformed

